        public static GridNode Get(int index) {
            if (index < 0) {
                // Negative value indicates a test to run
                index = -index - 1;
                if (index < Test.Length) {
                    return new GridNode(Test[index]);
                }
            } else if (index > 0) {
                // Positive value indicates an actual level
                index = index - 1;
                if (index < Game.Length) {
                    return new GridNode(Game[index]);
                }
            }
            return null;
        }
